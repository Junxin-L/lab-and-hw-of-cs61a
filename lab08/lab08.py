    #观察发现是原始节点数据的累乘，而不是修改过的，所以应该从辈份最高的开始，
    def mul(t):
      if t.is_leaf():
        return t.label
      else:
        num = t.label
        for b in t.branches:
          num *= mul(b)
        return num
      
    t.label = mul(t)
    if t.is_leaf():
      return
    else:
      for b in t.branches:
        b.label = mul(b)

